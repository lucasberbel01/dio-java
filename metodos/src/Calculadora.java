public class Calculadora {

    public void somar(double n1, double n2) {
        System.out.println("A soma dos valores é: " + (n1 + n2));
    }

    public void subtrair(double n1, double n2) {
        System.out.println("A subtracao dos valores é: " + (n1 - n2));
    }

    public void dividir(double n1, double n2) {
        System.out.println("A dividao dos valores é: " + (n1 / n2));
    }

    public void multiplicar(double n1, double n2) {
        System.out.println("A multiplicacao dos valores é: " + (n1 * n2));
    }


}

